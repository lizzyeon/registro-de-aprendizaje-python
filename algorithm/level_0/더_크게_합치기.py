# 수정 풀이3

def solution(a, b):
    x, y = str(a), str(b)
    return int(x+y) if x+y>y+x else int(y+x)

# ----------------------------------------------------------------
# 문자열 변환 변수 x, y를 한 줄로 표현할 수 있다
# solution으로 받은 숫자 : 숫자 -> 문자열로 변환 비교 -> 정수 값 도출 순


