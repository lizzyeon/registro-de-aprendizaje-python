# 수정 풀이1

def solution(a, b):
    return a+b if a+b > b+a else b+a

# ----------------------------------------------------------------
# 이 문제는 숫자 비교가 아닌 '문자열' 비교이다.
# a+b는 숫자 계산이고, 따라서 문자열로 바꿔 계산해야한다
# a+b와 b+a가 문자이지만 길이가 같아서 앞의 숫자부터 크기 비교를 하기 때문에 문제x


# ================================================================
# 수정 풀이2

def solution(a, b):
    x = str(a)
    y = str(b)
    return x+y if x+y>y+x else y+x

# ----------------------------------------------------------------
# 문자열로 바꿔서 크기 비교를 했다. 하지만 반환값은 또 숫자로 해야하는 것,,


