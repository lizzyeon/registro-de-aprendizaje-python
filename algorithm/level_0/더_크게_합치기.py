# 다른 풀이 1

def solution(a, b):
    a, b = str(a), str(b)
    return int(max(a+b, b+a))

# ----------------------------------------------------------------
# a, b를 문자열로 바꿔서 크기비교를 'max()' 함수를 이용했다


