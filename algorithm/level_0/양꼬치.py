# 요약: 12000짜리 양꼬치 n개, 2000짜리 음료 k개, 10인분에 음료 하나 서비스의 가격


# ================================================================
# 내 풀이
  
def solution(n, k):
    return 12000 * n + 2000 * (k - int(n/10))
      
# ----------------------------------------------------------------
# int(n/10) 대신 n//10 써도 됨
